    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultCategoryDataset dataset=new DefaultCategoryDataset();
        dataset.setValue(DieselCar.getCountFemale(), "Female-Male", "Diesel Female");
        dataset.setValue(DieselCar.getCountMale(), "Female-Male", "Diesel Male");
        dataset.setValue(GasolineCar.getCountFemale(), "Female-Male", "Gas. Female");
        dataset.setValue(GasolineCar.getCountMale(), "Female-Male", "Gas. Male");
        dataset.setValue(ElectricCar.getCountFemale(), "Female-Male", "Electric Female");
        dataset.setValue(ElectricCar.getCountMale(), "Female-Male", "Electric Male");
        dataset.setValue(Repair.getCountFemale(), "Female-Male", "Total Female");
        dataset.setValue(Repair.getCountMale(), "Female-Male", "Total Male");
        JFreeChart chart=ChartFactory.createBarChart3D("Gender Statistics", "Female-Male", "COUNT", dataset, PlotOrientation.VERTICAL, false , true, false);
        CategoryPlot p=chart.getCategoryPlot();
        chart.setBackgroundPaint(Color.CYAN);        
        chart.setBorderPaint(Color.RED);
        chart.getTitle().setBackgroundPaint(Color.white);
        p.setRangeGridlinePaint(Color.blue);
        ChartFrame frame=new ChartFrame("Bar Chart ve Sakatlar",chart);
        frame.setVisible(true);
        frame.setSize(1100,779);
    }//GEN-LAST:event_jButton3ActionPerformed

